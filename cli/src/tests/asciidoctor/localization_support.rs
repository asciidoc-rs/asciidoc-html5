// `version-label` is the one built-in label `adoc` emits today: it names the
// revision number in the header (Asciidoctor downcases it). Passing a
// translated value with `-a` changes the rendered label.
#[test]
fn version_label_attribute_translates_the_revision_label() {
    verifies!(
        r#"
|`version-label`
|Version
|

"#
    );

    // `-a version-label=Ausgabe` retitles the revision label; the header renders
    // it downcased, matching Asciidoctor.
    let translated = run_piped(
        &["adoc", "-a", "version-label=Ausgabe", "-"],
        "= Titel\nAutor\nv2.5, 2026-01-04\n",
    );
    assert!(translated.contains("<span id=\"revnumber\">ausgabe 2.5,</span>"));

    // The default value is the English-derived "version".
    let default_html = run_piped(&["adoc", "-"], "= Titel\nAutor\nv2.5, 2026-01-04\n");
    assert!(default_html.contains("<span id=\"revnumber\">version 2.5,</span>"));
}


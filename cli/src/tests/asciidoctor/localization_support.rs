// Asciidoctor's "Localization Support" page, tracked from the CLI crate. Most
// of the page describes features `adoc` does not implement -- DocBook-toolchain
// label translation, the full table of built-in caption/label attributes, the
// `lang`-driven `locale/attributes.adoc` include, and content translation -- so
// those spans are non-normative here. Three claims have command-line
// counterparts this crate verifies: UTF-8 content passes through unchanged,
// `-a lang=es` is only a hint (it does not translate built-in labels for HTML),
// and `-a version-label=…` retitles the one built-in label `adoc` emits today,
// the revision label in the header. The `asciidoc-html5` crate verifies the
// same three claims through the API, and the sdd tool merges the two crates'
// coverage.

/// Pipes `source` through `adoc` with `args` (which select standard input),
/// returning the captured standalone HTML. Drives the real stdin read path via
/// [`run_with_input`], the injectable-reader core of `run`.
fn run_piped(args: &[&str], source: &str) -> String {
    let cli = Cli::parse_from(args);
    let mut stdin = source.as_bytes();
    let mut stdout = Vec::new();
    run_with_input(&cli, &mut stdin, &mut stdout).expect("adoc converts");
    String::from_utf8(stdout).expect("adoc output is UTF-8")
}


// The `lang` attribute is only a hint: `-a lang=es` sets the html `lang` but
// does not translate any built-in label for HTML output.
#[test]
fn lang_does_not_translate_built_in_labels() {
    verifies!(
        r#"
The `lang` attribute _does not_ enable automatic translation of built-in labels when converting directly to HTML or PDF.
It's merely a hint to configure the DocBook toolchain.
If you're not using the DocBook toolchain for publishing, you must translate each built-in label yourself.
One way is to set the following attributes in the document header or by passing the attributes via the API or CLI:

"#
    );

    // `-a lang=es` records the language on the html element, but the built-in
    // revision label stays the English-derived "version" -- no automatic
    // translation happens; the author must set the label attribute themselves.
    let html = run_piped(
        &["adoc", "-a", "lang=es", "-"],
        "= Título\nAutora\nv1.0, 2026-01-04\n",
    );
    assert!(html.contains("<html lang=\"es\">"));
    assert!(html.contains("<span id=\"revnumber\">version 1.0,</span>"));
}


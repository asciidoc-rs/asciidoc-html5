// The walkthrough's core CLI step: `asciidoctor my-document.adoc` converts the
// file silently and, with no `-o`, derives the output name `my-document.html`
// from the input. This crate's `adoc` command mirrors that behavior, which this
// test drives end to end.
#[test]
fn converts_and_derives_output_file() {
    // The "Convert an AsciiDoc document to HTML" section and the command that
    // carries it out. Nothing is printed on success.
    verifies!(
        r#"
=== Convert an AsciiDoc document to HTML

To convert [.path]_my-document.adoc_ to HTML from the command line:

. Open a terminal.
. Switch to the directory that contains the [.path]_my-document.adoc_ document
. Call the Asciidoctor processor with the `asciidoctor` command, followed by the name of the document.
+
--
 $ asciidoctor my-document.adoc

"#
    );

    non_normative!(
        r#"
Remember, Asciidoctor's default converter is html5, so it isn't necessary to specify it with the `-b` command.
--

. You won't see any messages printed to the console.
Type `ls` to view the files in the directory or navigate to the directory in a file manager.
"#
    );

    // Listing the directory shows the derived _my-document.html_ file, whose name
    // Asciidoctor takes from the input document.
    verifies!(
        r#"
You should see a new file named [.path]_my-document.html_.
+
--
 $ ls
 my-document.adoc  my-document.html

Asciidoctor derives the name of the output document from the name of the input document.
"#
    );

    // Drive the closest equivalent of `asciidoctor my-document.adoc`: hand the
    // `adoc` CLI a `<name>.adoc` file with no `-o`, and confirm it derives
    // `<name>.html` (as `asciidoctor` derives `my-document.html`), writes a
    // complete HTML5 document there, and prints nothing.
    let source = "= Hello\n\nWorld.";
    let path = std::env::temp_dir().join(format!("adoc-html-backend-{}.adoc", std::process::id()));
    let derived = path.with_extension("html");
    std::fs::write(&path, source).expect("write temp input");

    let cli = Cli::parse_from(["adoc", path.to_str().expect("temp path is UTF-8")]);
    let mut stdout = Vec::new();
    run(&cli, &mut stdout).expect("adoc converts the file");

    assert!(stdout.is_empty(), "adoc wrote to stdout on success");
    assert!(
        derived.exists(),
        "adoc did not create the derived output file"
    );
    let html = std::fs::read_to_string(&derived).expect("read derived output file");
    let _ = std::fs::remove_file(&path);
    let _ = std::fs::remove_file(&derived);

    assert!(html.starts_with("<!DOCTYPE html>"));
    assert!(html.contains("<title>Hello</title>"));
    assert!(html.trim_end().ends_with("</body>\n</html>"));
}


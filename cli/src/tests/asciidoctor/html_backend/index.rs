track_file!("ref/asciidoctor/docs/modules/html-backend/pages/index.adoc");

// Asciidoctor's "Generate HTML from AsciiDoc" page, tracked from the CLI crate.
// The page documents the default HTML output that `adoc` produces, so several
// of its CLI-facing claims have counterparts here: the output file carries the
// `.html` extension (see `output_file_extension_is_html`), running `asciidoctor
// my-document.adoc` converts the file and derives the `my-document.html` output
// name (see `converts_and_derives_output_file`), and the `-o -` form used to
// preview the HTML writes it to standard output (see
// `previews_html_on_stdout`). The `asciidoc-html5` crate verifies the API-level
// conversion, and the sdd tool merges the two crates' coverage.
//
// The rest is non-normative here: the converter-class and backend-registration
// facts are Asciidoctor internals, the Google Fonts warning concerns a default
// stylesheet this project does not embed yet, and the screenshot step has no
// CLI counterpart. The "Generate XHTML" section (and the sentence introducing
// it) is tracked as non-normative by deliberate project decision: `adoc` emits
// HTML5 syntax only and does not support Asciidoctor's `xhtml`/`xhtml5`
// backends (see the workspace README).

non_normative!(
    r#"
= Generate HTML from AsciiDoc
:navtitle: Generate HTML

Asciidoctor's default output format is HTML (specifically HTML 5).
That means when you run Asciidoctor without any parameters, it will produce HTML.
This page explains how to use Asciidoctor to convert AsciiDoc to HTML.

== Backend and converter

Asciidoctor's built-in HTML converter is registered for the `html` and `html5` backends.
The HTML converter generates HTML 5 that's styled with CSS3.
It may include JavaScript to support additional integrations, such as client-side syntax highlighting.

[horizontal]
Backend names:: `html`, `html5`
Converter class:: `Asciidoctor::Converter::Html5Converter`
Output format:: HTML
"#
);

// The horizontal list's final term: the HTML output file carries the `.html`
// extension. With no `-o`, `adoc` derives the output file name from the input
// by swapping its extension for `.html`, so its output always ends in `.html`.
#[test]
fn output_file_extension_is_html() {
    verifies!(
        r#"
Output file extension:: .html

"#
    );

    let source = "= Hello\n\nWorld.";
    let path =
        std::env::temp_dir().join(format!("adoc-html-backend-ext-{}.adoc", std::process::id()));
    let derived = path.with_extension("html");
    std::fs::write(&path, source).expect("write temp input");

    let cli = Cli::parse_from(["adoc", path.to_str().expect("temp path is UTF-8")]);
    let mut stdout = Vec::new();
    run(&cli, &mut stdout).expect("adoc converts the file");
    let wrote_derived = derived.exists();
    let _ = std::fs::remove_file(&path);
    let _ = std::fs::remove_file(&derived);

    assert!(stdout.is_empty(), "adoc wrote to stdout on success");
    assert_eq!(
        derived.extension().and_then(|e| e.to_str()),
        Some("html"),
        "derived output name should carry the .html extension"
    );
    assert!(
        wrote_derived,
        "adoc did not create the derived .html output file"
    );
}

